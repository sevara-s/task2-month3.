// math operators in javascript

// Addition (+) helps to add two variables in javascript

let a = 50;
let b= 90;
console.log(a+b);

// Substraction (-)

console.log(b-a);

// Multiplication (*)

console.log(a * b);

// Exponentation (**)

console.log(a ** 3);

// Division (/)

console.log(b/3);

// Modulus (%)

console.log(b%a);

// Increment (++)

console.log(a++);
console.log(++a);

// decrement(--)
console.log(a--);
console.log(--a);

